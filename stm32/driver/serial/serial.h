#ifndef SERIAL_H_
#define SERIAL_H_
/******************************************************************************/
/**!                               INCLUDE                                    */
/******************************************************************************/

/******************************************************************************/
/**!                            PUBLIC TYPEDEF                                 */
/******************************************************************************/
typedef void(*serial_callback_t)(uint8_t byte);

typedef enum
{
	SERIAL_BAUDRATE_115200 = 115200,
	SERIAL_BAUDRATE_9600 = 9600,
}Serial_baudrate_t;

typedef struct
{
	Serial_baudrate_t  baud;
	serial_callback_t  callback;
}Serial_t;
/******************************************************************************/
/**!                            PUBLIC SYMBOLS                                 */
/******************************************************************************/

/******************************************************************************/
/**!                         EXPORTED VARIABLES                               */
/******************************************************************************/

/******************************************************************************/
/**!                        EXPORTED FUNCTIONS                                */
/******************************************************************************/
void Serial_Init (Serial_t *serial);
void Serial_SendByte(uint8_t byte);
void Serial_SendData(uint8_t* data, uint8_t len);
#endif /* SERIAL_H_ */
